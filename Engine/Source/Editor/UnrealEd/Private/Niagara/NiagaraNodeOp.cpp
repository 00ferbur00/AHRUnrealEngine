UNiagaraNodeOp::UNiagaraNodeOp(const FObjectInitializer& ObjectInitializer)
	: Super(ObjectInitializer)
{
}

const TCHAR* UNiagaraNodeOp::InPinNames[3] =
{
	TEXT("A"),
	TEXT("B"),
	TEXT("C")
};

void UNiagaraNodeOp::AllocateDefaultPins()
{
	const UEdGraphSchema_Niagara* Schema = GetDefault<UEdGraphSchema_Niagara>();
	VectorVM::FVectorVMOpInfo const& Info = VectorVM::GetOpCodeInfo(OpIndex);

	UScriptStruct* VectorStruct = FindObjectChecked<UScriptStruct>(UObject::StaticClass(), TEXT("Vector"));
	for (int32 SrcIndex = 0; SrcIndex < 3; ++SrcIndex)
	{
		VectorVM::EOpSrc::Type PinType = Info.SrcTypes[SrcIndex];
		if (PinType != VectorVM::EOpSrc::Invalid)
		{
			UEdGraphPin* Pin = CreatePin(EGPD_Input, Schema->PC_Struct, TEXT(""), VectorStruct, false, false, InPinNames[SrcIndex]);
			check(Pin);
			Pin->bDefaultValueIsIgnored = false;
			Pin->bDefaultValueIsReadOnly = false;
			Pin->bNotConnectable = false;
			Pin->DefaultValue = TEXT("1.0,1.0,1.0,1.0");
			Pin->AutogeneratedDefaultValue = TEXT("1.0,1.0,1.0,1.0");
		}
	}

	CreatePin(EGPD_Output, Schema->PC_Struct, TEXT(""), VectorStruct, false, false, TEXT("Result"));
}

FText UNiagaraNodeOp::GetNodeTitle(ENodeTitleType::Type TitleType) const
{
	VectorVM::FVectorVMOpInfo const& Info = VectorVM::GetOpCodeInfo(OpIndex);
	return FText::FromString(Info.FriendlyName);
}

FLinearColor UNiagaraNodeOp::GetNodeTitleColor() const
{
	return GetDefault<UGraphEditorSettings>()->FunctionCallNodeTitleColor;
}


